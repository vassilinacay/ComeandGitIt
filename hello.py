print("\nCALCULATOR IN PYTHON")

first_num = int(input("\nEnter first number: "))
second_num = int(input("Enter second number: "))

sum = first_num + second_num
diff = first_num - second_num
prod = first_num * second_num
floor_div = first_num // second_num
mod = first_num % second_num

print(f"\n{first_num} + {second_num} = {sum}")
print(f"{first_num} - {second_num} = {diff}")
print(f"{first_num} * {second_num} = {prod}")
print(f"{first_num} // {second_num} = {floor_div}")
print(f"{first_num} % {second_num} = {mod}")